package zadaca1;

public class Student {
	//Deklariranje na atributi
		public String firstName;
		public String lastName;
		public int index;

		//Default constructor
		public Student ( ) {

		}

		public Student (String firstName, String lastName, int index) {
			this.firstName = firstName;
			this.lastName = lastName;
			this.index = index;
		}
}
